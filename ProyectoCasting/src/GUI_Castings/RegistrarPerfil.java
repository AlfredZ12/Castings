    private void GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarActionPerformed
        Perfil aux = new Perfil();   
         aux.setEstado(this.txtEstado.getText());
         aux.setColorcabello((String) this.boxPelo.getSelectedItem());
         aux.setColorojos((String) this.boxOjos.getSelectedItem());
         aux.setRangoaltura((String) this.boxAltura.getSelectedItem());
         aux.setRangoedad((String) this.boxEdad.getSelectedItem());
         aux.setEspecialidad((String) this.boxEspecialidad.getSelectedItem());   
         aux.setSexo((String) this.boxSexo.getSelectedItem());
         
         int SeleccionTbl = tablaCastings.getSelectedRow();
        
         if (SeleccionTbl != -1) {
            Integer id = (Integer) tablaCastings.getValueAt(SeleccionTbl, 0);
            Casting presencial = persistencia.BuscarCasting(id);
            presencial.setIdPerfil(aux);

            persistencia.Agregar(aux);
            persistencia.Actualizar(presencial);
            
            if (control.MensajeNuevo(this, "Desea Agregar otro Perfil", "Mensaje!")) {
                vaciar();
            } else {
                JOptionPane.showMessageDialog(this, "Se agrego Correctamente el perfil con id"
                        + aux.getIdperfil() + " al Casting con id: " + presencial.getIdcasting());
                dispose();
            }

        }


    }//GEN-LAST:event_GuardarActionPerformed

